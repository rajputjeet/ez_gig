  final int id;
  final String name;
  final String image;
  final double rating;
  final int experienceYears;
  final String location;
  final String role;

  const TalentEntity({
    required this.id,
    required this.name,
    required this.image,
    required this.rating,
    required this.experienceYears,
    required this.location,
    required this.role,
  });
}
